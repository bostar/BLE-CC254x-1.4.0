void CFGProcess(uint8 cmd)
{
  switch(cmd)
  {
    case 0x00:   //允许限时加入网络
      XBeeSetNJ(XBeeUartRec.data[19],NO_RES);
    break;
    case 0x02:   //入网响应
      if(*(&cmd+1) == 0x01)  //允许入网
        FlagJionNet = NetOK;  //组网成功后，进入正常工作模式
      else   //禁止入网
      {
        XBeeLeaveNet();  
        FlagJionNet = NetOK;
        osal_set_event( XBeeTaskID, XBEE_IDLE_EVT ); //进入空闲模式
        UartCtl = 1;   //关闭串口
        //zigbee进入休眠
      } 
      break;
    case 0x03:
      XBeeLeaveNet();
      XBeeLeaveNet();  
      FlagJionNet = NetOK;
      osal_set_event( XBeeTaskID, XBEE_IDLE_EVT ); //进入空闲模式
      UartCtl = 1;   //关闭串口
      //zigbee进入休眠
      break;
    default:
    break;
  }
}
