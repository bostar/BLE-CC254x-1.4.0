void CTLProcess(uint8 cmd)
{
  switch(cmd)
  {
    case 0:
      if(XBeeUartRec.data[19] == 0)  //解锁
      {
        MotorForward();
      }
      if(XBeeUartRec.data[19] == 1)  //锁定
      {
        MotorReverse();
      }
      break;
    default:
      break;
  }
}
