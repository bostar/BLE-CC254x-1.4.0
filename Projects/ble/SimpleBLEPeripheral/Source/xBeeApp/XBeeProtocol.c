uint16 XBeeLockState(LockStateType LockState)
{
  uint8 data[5];
  
  data[0]   =   'C';
  data[1]   =   'T';
  data[2]   =   'L';
  data[3]   =  0x01;
  data[4]   =  LockState;
  
  return XBeeSendToCoor(data,5,NO_RES);
}
